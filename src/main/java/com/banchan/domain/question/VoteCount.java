package com.banchan.domain.question;

public class VoteCount {
    int total, ans_a, ans_b;

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getAns_a() {
        return ans_a;
    }

    public void setAns_a(int ans_a) {
        this.ans_a = ans_a;
    }

    public int getAns_b() {
        return ans_b;
    }

    public void setAns_b(int ans_b) {
        this.ans_b = ans_b;
    }
}
